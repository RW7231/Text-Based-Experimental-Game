public class Monster {

    public int Health, Attack, AC, Defense, Type, soulValue;

    public Monster(int Health, int Attack, int AC, int Defense, int Type, int soulValue){
        this.Health = Health;
        this.Attack = Attack;
        this.AC = AC;
        this.Defense = Defense;
        this.Type = Type;
        this.soulValue = soulValue;
    }


}
